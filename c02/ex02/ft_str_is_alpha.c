int	ft_str_is_alpha(char *str)
{	
	int	i;
	int	alpha;

	i = 0;
	alpha = 1;
	while (str[i] != '\0')
	{	
		if (str[i] < 65 || (str[i] >= 91 && str[i] <= 96) || str[i] > 122)
		{
			alpha = 0;
			break ;
		}
		i++;
	}
	return (alpha);
}
