int	ft_str_is_numeric(char *str)
{	
	int	i;
	int	numeric;

	i = 0;
	numeric = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 48 || str[i] > 57)
		{
			numeric = 0;
			break ;
		}
		i++;
	}
	return (numeric);
}
