int	ft_strcmp(char *s1, char *s2)
{
	int		i;
	int		equal;

	i = 0;
	equal = 0;
	while (*(s1 + i) != '\0' && *(s2 + i) != '\0')
	{
		if (*(s1 + i) != *(s2 + i))
		{
			break ;
		}
		i++;
	}
	equal = *(s1 + i) - *(s2 + i);
	return (equal);
}
