int	ft_find_next_prime(int nb)
{
	int		i;
	int		limit;
	
	

	if (nb <= 2)
		return (2);
	while (1)
	{	
		if (nb % 2 != 0 && nb % 3 != 0 && nb % 5 != 0 && nb % 7 != 0 && nb % 11 != 0)
		{
			i = 3;
			while (i < limit)
			{
				if (nb % i == 0)
				{
					i = -1;
					break ;
				}
				i += 2;
			}
			if (i == -1)
				return (nb);
		}
		nb++;
	}
	return (0);
}
